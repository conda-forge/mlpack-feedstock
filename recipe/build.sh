mkdir -p build
cd build

cmake \
        -G "${CMAKE_GENERATOR}" \
	-DCMAKE_INSTALL_PREFIX="${PREFIX}" \
	-DCMAKE_PREFIX_PATH="${PREFIX}" \
	-DBUILD_PYTHON_BINDINGS=ON \
	-DBUILD_CLI_EXECUTABLES=ON \
	-DBUILD_SHARED_LIBS=ON \
	-DBUILD_TESTS=OFF \
	-DBOOST_ROOT="${PREFIX}/include/boost" \
	-DUSE_OPENMP=ON \
	-DDEBUG=OFF \
	-DPROFILE=OFF \
	../

make -j${CPU_COUNT}
make install
